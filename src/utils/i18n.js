export function getTranslations(language) {
  return translations[language] ?? translations.en;
}

export function translateDifficulty(difficulty, language) {
  return getTranslations(language).difficulties[difficulty] ?? difficulty;
}

export function translateMode(mode, language) {
  return getTranslations(language).modeNames[mode] ?? mode;
}
